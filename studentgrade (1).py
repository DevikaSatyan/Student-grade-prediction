import pandas as pd
import numpy as np
import sklearn
from sklearn import linear_model
from sklearn.utils import shuffle

dataset=pd.read_csv("student-por.csv",sep=";")#to separate each attribute by ; or space
print(dataset)
print(dataset.shape)
len(dataset)

#there are 33 attributes, but we need only some of the attributes
dataset=dataset[["G1","G2","G3","absences","studytime","failures"]]
print(dataset.head())

predict="G3"
X=np.array(dataset.drop([predict],1))
y=np.array(dataset[predict])
len(y)

X_train,X_test,y_train,y_test=sklearn.model_selection.train_test_split(X,y,test_size=0.1)
print(len(X_train))
print(len(y_train))

linear=linear_model.LinearRegression()
linear.fit(X_train,y_train)

acc=linear.score(X_test,y_test)
print(acc)

print("coef :",linear.coef_)
print("intercepts :",linear.intercept_)